                <h2>Adjective: Definition & Types</h2>
<p>An adjective is a word or term that is used to describe the qualities or features of nouns and pronouns in a sentence or a paragraph. It notifies the shape, time, quality, quantity, emotions, and further more things about a noun.Adjectives serve the purpose of giving authentic and important information about the nouns and pronouns. Adjectives enhances the sentence by including precision, and originality to it.</p>
           
<h3>Example</h3>
                <p>The team has a fast baller. (What kind?) <br>I have twelve candies in my pocket. (How many?)</p>

                <p>I loved that blue car. (Which one?)<br> I earn more money than he does in a year. (How much?)</p>
                <p>Adjectives are used to answer questions regarding the features, skills or the nature of subject. However, the most frequent use of adjectives in the parts of speech in sentences. There are various types of adjectives that are used according to the nature of sentences.</p>


