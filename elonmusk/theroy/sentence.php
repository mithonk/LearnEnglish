                <h2>Sentence: Definition & Types</h2>
<p>Learning how to make accurate and correct sentences is probably the most important part of <br>English or in the grammar of any other language. Your skill to form sentences also allows to<br> speak that particular language as well. Normally a sentence begins with a capital letter and is<br> ended with a punctuation mark depending upon its nature and type.</p>
 <p>It can be defined as a collection of words joined up to form a single sentence that can<br> accurately deliver or express what an individual is trying to convey. Mostly a sentence ends on<br> a full stop but it can also end on a question mark if the nature of that particular sentence is<br> interrogative. Signs such as exclamations marks and colons or semicolons are also used <br>depending upon the sentence structure.</p>               
<h3>Example</h3>
<p>He is an intelligent boy (statement), is he an intelligent boy? (Question), <br>what a nice view! (Exclaiming)</p>
 <p>In simple words, it can be concluded that a sentence should have three primary parts or <br>characteristics</p>
