                <h2>Noun: Definition & Types</h2>
<p>Any word or term that is entitled to identify a person, place, animal, thing, ideas or events are <br>known as Nouns. Majority of the words fall under the category of nouns regardless of language<br> specifications.</p>
           
<h3>Example</h3>
                <p>Person – a name for a person: – Jason, lilly, Ron, Ben, Brat, etc.</p>

                <p>Animal – a name for an animal: – sheep, kitten, calf, puppy, etc.</p>

<p>Place – a name for a place: – Australia, Belgium, Iceland, Norway, etc.</p>

<p>Thing – a name for a thing: – brick, brush, clock, gate, pen, box, etc.</p>

                <p>Idea – A name for an idea: – motivation, suspicion, anger, jealousy, etc.</p>
                <h3>Different Types of Noun:</h3>
                <ul>
                <li>Proper Noun</li>
<li>Common Noun</li>
<li>Abstract Noun</li>
<li>Concrete Noun</li>
<li>Countable Noun</li>
<li>Non-countable Noun</li>
<li>Collective Noun</li>
<li>Compound Noun</li></ul>
                <h4>Proper Noun:</h4>
                <p>A noun that refers only to a single person, place, animal, or thing in a way that it differentiates <br>that particular subject from others on the basis of their certain feature or skill is known as a <br>proper noun.</p>
             <p>Example: Dubai (it refers to only one particular city)</p>
                <h4>Common Noun:</h4>
                <p>The words that are used to refer something that is extremely common and not famous are called <br>common nouns.</p>
             <p>Example: State (it can refer to any state, nothing in particular)</p>
                 <h4>Abstract Noun:</h4>
                <p>Abstract nouns are used for things that do not keep a physical existence. Such nouns are mostly<br> used to denote ideas, skills, terms, etc.</p>
             <p>Example: happiness, sadness, anger, etc.</p>

 

            
