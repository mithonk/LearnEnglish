                <h2>Pronoun: Definition & Types</h2>
<p>Pronouns are used to denote a noun that was used earlier in a sentence or in a paragraph. <br>Pronouns are used to avoid repetition of a specific personality, place or any other noun.</p>
           
<h3>Example</h3>
                <p>Michael is a good boy. He gets up early in the morning. <br>(Here, you don’t have to mention ‘Michael’ again)</p>

                <p>The coach selected several key points. He wanted the team to memorize them.<br> (‘He’ replaces ‘the coach’; ‘them’ replaces ‘several key points’)</p>


                <h3>Different Types of Pronoun:</h3>
                <ul>
                <li>Subject Pronouns</li>
<li>Object Pronouns</li>
<li>Possessive Pronouns</li>
<li>Reflexive Pronouns</li>
<li>Intensive Pronouns</li>
<li>Demonstrative Pronouns</li>
<li>Interrogative Pronouns</li>
</ul>
                <h4>Subject Pronouns:</h4>
                <p>The words that are included to replace the subject or the verb in a sentence are known as subject<br> pronouns. It is used to replace nouns and verbs that are used in the previous sentence.</p>
                <p>Example:</p><p>
                <p>Mike can’t attend the party. He has gone to his grandparents.</p>

                <p>Marta is a good storyteller. She told a ghost story that scared everyone.</p>

                <p>Instead of saying: Sara goes to school on foot because Sara likes to walk.</p>

<p>We say: Sara goes to school on foot because she likes to walk.</p>
                <h4>Object Pronouns:</h4>
                <p>Object pronouns function in the replacement of an object or an indirect object of a sentence. It<br> replaces the antecedent object from a sentence in order to avoid repetition.<br> Object pronouns are used with prepositions as well.</p>
                <p>They are: me, us, you, them, her, him, it.</p>
             <p>Example: My father gave me a present and I want you to see it.</p>
                 <h4>Possessive Pronouns:</h4>
                <p>Pronouns that are used to replace nouns used with possessive adjectives are called Possessive pronouns. Words such as my, her, his, mine, hers, etc. are known as possessive pronouns</p>
             <p>My becomes: mine.</p>

<p>Your becomes: yours.</p>

 

            
