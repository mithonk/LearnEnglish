                <h2>Verb: Definition & Types</h2>
                
                
<p>A word or combination of words that is used to highlight any action of a living being or condition is denoted as a verb. It is an important part of a sentence that informs us about the action that the subject is performing. Verbs can be considered as the core part of English sentences.</p>
                

           
<h3>Example</h3>
                <p>Walking</p>
                
                <p>Running</p>
                <p>Drinking</p>
                
                <p>Verbs are linked to a lot of other terms such as subject, person, number, tense, mood, voice, etc.</p>
                


<h4>Basic Forms of Verbs</h4>
                <p>Primarily there are six forms of verbs such as</p>
                
                <p>Base form: People walk in the field</p>
                <p>Infinitive: tell them not to break the glass</p>
                <p>Past Tense: Children played soccer yesterdayn</p>
                
                <p>Past Participle: I have eaten my pizza</p>
                <p>Present Participle: I saw them going towards the park</p>
                <p>Gerund: Running is the best exercise</p>
                
 <h3>Different Types of Verbs</h3>
                             <ul>
<li>Finite Verbs</li>
<li>Non-finite Verbs</li>
<li>Action Verbs</li>
<li>Linking Verb</li>
<li>Auxiliary Verbs</li>
<li>Modal Verbs</li>

</ul>
                
                
 <h4>Finite Verbs</h4>
                
                 
                <p>Finite verbs are the only form of the verb that is performed or refers to a subject that uses twelve forms of tenses and changes according to the subject or the person referred. Finite verbs are also known as roots of the sentences.</p>
                
                
                
                
      


                <h3>Tall adjectives:</h3>
                <P>Tall adjectives are adjectives that consist of 3 or more syllables. such as: intelligent, sufficient, etc.</P>
                <p>When comparing between two using these tall adjectives, we do as follows:</p>
                <P>……more/ less + tall adjective + than……</P>
                
 <h5>For example:</h5>
                <p>Sony is intelligent. Leo is not intelligent.</p>
                <p>In this case, we have two ways for comparing between them:</p>
                <p>1.Sony is more intelligent than Leo</p>
                <p>2.Leo is less intelligent than Sony.</p>
                
 <h2>Superlative adjectives:</h2>
<h3>Short adjectives</h3>
                <p>Short adjectives are adjectives that consist of less than 3 syllables. They are either: one syllable only such as: short, tall, small or two syllables such as: easy, funny, pretty, etc.</p>
                <p>1.we add “the” before the adjective.</p>
                <p>2.we add + (-est) to the end of the adjective.</p>
                
<h3>Example</h3>
                <p>Sony is tall. Leo is short. Mea is short</p>
                <p>Sony is the tallest</p>
                <P>We may as well say</P>
                <p>Sony is the tallest of the three</p>
                
<h3>Notes:</h3>
                <p>If the adjective ends with (-y) preceded by a consonant as in: happy, when we add (-est) we replace the (-y) with (-iest) so it becomes (happiest)</p>
                <p>There are adjectives that ends with a consonant preceded by a short vowel such as: big, and there are adjectives that ends with a consonant preceded by a long vowel such as: fine.With the short vowel adjectives, we double the last letter so “big” as a superlative adjective becomes biggest.</p>
                <p>.For example: Sara’s room is the biggest room in our house.Whereas with  the long vowel adjectives, we do not double the last letter so “fine” as a superlative adjective becomes finest</p>
                
                <p>There are irregular forms of comparative adjectives, such as:
good: the bestbad: the worstlittle: the leastmuch, many: the mos</p>
                
                <p>far: the furthest/ the farthest</p>
                <p>late: the latest/ the last</p>
                
<h3>Tall adjectives:</h3>
                <P>Tall adjectives are adjectives that consist of 3 or more syllables. such as: intelligent, sufficient, etc.</P>
                <p>When using superlative adjectives between more than two using these tall adjectives,  we do as follows:</p>
                <p>……the most/ the least + tall adjective + ……</p>
                
<h4>Example</h4>
                <p>Sony is the most intelligent student in the classroom</p>
                
                
<h3>Note</h3>
                <p>There is a very common construction that you need to pay attention to</p>
                
                <p>the most/ short adjective + (est) ………. have/ has + ever + P.P</p>
                
                
 <h4>Example</h4>
                <p>This is the most beautiful girl I have ever seen</p>
                <p>This is the prettiest girl I have ever seen</p>
            
                

 

            
